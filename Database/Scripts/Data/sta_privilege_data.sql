MERGE INTO STA_PRIVILEGE a
USING
    (SELECT
         125 AS ID,
         q'[Leerkrachten Overzicht]' AS NAME,
         null AS DESCRIPTION
     FROM DUAL) b
ON ( a.ID = b.ID )
WHEN NOT MATCHED THEN
    INSERT (ID, NAME, DESCRIPTION)
    VALUES (b.ID, b.NAME, b.DESCRIPTION)
WHEN MATCHED THEN
    UPDATE SET
               a.NAME = b.NAME,
               a.DESCRIPTION = b.DESCRIPTION;

